// Function to trigger file download
function downloadJson(filename, jsonContent) {
    const textContent = JSON.stringify(jsonContent, null, 2); // Stringify with 2 spaces for readability
    const element = document.createElement('a');
    element.setAttribute('href', 'data:application/json;charset=utf-8,' + encodeURIComponent(textContent));
    element.setAttribute('download', filename);
    element.style.display = 'none'; // Hide the element
    document.body.appendChild(element);
    element.click(); // Simulate click
    document.body.removeChild(element); // Clean up
}

// Populate the list on page load
document.addEventListener('DOMContentLoaded', () => {
    const blockListElement = document.getElementById('blockList');

    blockData.forEach(item => {
        const listItem = document.createElement('li');
        listItem.classList.add('block-item');

        // Display the full identifier
        const nameSpan = document.createElement('strong');
        // Access the identifier correctly: item.content.minecraft.block.description.identifier
        nameSpan.textContent = item.content.minecraft.block.description.identifier;

        const downloadButton = document.createElement('button');
        downloadButton.textContent = `Download ${item.filename}`;
        downloadButton.addEventListener('click', () => {
            downloadJson(item.filename, item.content);
        });

        listItem.appendChild(nameSpan);
        listItem.appendChild(downloadButton);
        blockListElement.appendChild(listItem);
    });
});